	NSURL *soundFile = [[NSURL alloc] initFileURLWithPath:pathForSilentFile];
	AVAudioPlayer *sPlayer = [[AVAudioPlayer alloc] initWithContentsOfURL:soundFile error:NULL];
	[soundFile release];
	[sPlayer prepareToPlay];
	
	[sPlayer play];
}

